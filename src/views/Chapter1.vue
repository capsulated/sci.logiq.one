                подготовленными Росаэронавигацией (Росавиацией) 20 августа 2007 г. одобрены на заседании Комиссии
                Правительства Российской Федерации по законопроектной деятельности.
            </p>
            <p>Предлагаемые изменения предусматривают дополнение законодательства, которые позволят в дальнейшем:
            </p>
