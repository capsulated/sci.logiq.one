                доступа к ресурсам аэронавигационной системы, необходимым для удовлетворения их эксплуатационных
                потребностей и обеспечения высокого уровня безопасности воздушного движения.
            </p>
            <p>
                В настоящее время сложилась ситуация, при которой:
            </p>
            <ul>
