                    в области аэронавигационной информации, устарели, не соответствуют стандартам ИКАО и международной
                    практике, мешают гармонизации и интеграции Аэронавигационной системы России с общемировой системой;
                </li>
                существующие службы аэронавигационной информации разобщены по ведомственному принципу, что затрудняет
                <li>оперативный доступ пользователей к требуемой аэронавигационной информации;</li>
                <li>ограниченный объем информационного содержания АИП России не удовлетворяет потребностей пользователей
                    АНИ;</li>
