                    деятельность по сбору, обработке, хранению и распространению государственной АНИ на федеральном и
                    региональном уровнях, и укомплектование их штатной численностью с одновременным решением вопроса
                    подготовки и повышения квалификации персонала;
                </li>
                <li>разработка программного обеспечения (с последующей сертификацией) для автоматизированного сбора,
                    обработки, хранения и распространения государственной АНИ, позволяющего потребителям работать в
                    едином формате и осуществлять обмен АНИ в электронном виде;
                </li>
                <li>внедрить систему качества (менеджмента) в систему обработки аэронавигационной информации,
                    совершенствовать процедуры и процессы управления качеством на каждой функциональной стадии обработки
                    аэронавигационных данных от аэропорта через предприятия, осуществляющие деятельность в области
                    аэронавигационной информации, до потребителя;
                </li>
                <li>создать корпоративную электронную сеть для функционирования автоматизированной системы сбора,
                    обработки, хранения и распространения АНИ;
                </li>
                <li>создать федеральную электронную базу данных АНИ, предусмотрев ее защиту от несанкционированного
                    доступа;
                </li>
                <li>осуществлять контроль за качеством вносимых в структуру воздушного пространства изменений, до их
                    опубликования в государственных документах АНИ;
                </li>
                <li>совершенствовать порядок обеспечения АНИ пользователей воздушного пространства с учетом Стандартов и
                    Рекомендуемой практики ИКАО;
                </li>
                <li>разработать концепцию развития системы обеспечения АНИ в свете внедрения программы CNS/ATM в
                    отечественную практику;
                </li>
                <li>разработать порядок обеспечения государственной АНИ пользователей воздушного пространства,
                    осуществляющих свою деятельность при уведомительном порядке использования воздушного пространства;
